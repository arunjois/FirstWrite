package Shankari.core;


public class Date {
    int day,mon,year;

    public void setDay(int day) {
        this.day = day;
    }

    public void setMon(int mon) {
        this.mon = mon;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public int getDay() {
        return day;
    }

    public int getMon() {
        return mon;
    }

    public int getYear() {
        return year;
    }


    public Date(int d, int m, int y){
        day = d;
        mon = m;
        year = y;
    }
}
